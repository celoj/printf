#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char d;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
		}
		else
		{
			n *= -1;
			write(fd, "-", 1);
			ft_putnbr_fd(n, fd);
		}
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n < 10)
	{
		d = n + '0';
		write(fd, &d, 1);
	}
}
