#include "libft.h"

void	ft_putnbr(int n)
{
	char d;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
		}
		else
		{
			n *= -1;
			write(1, "-", 1);
			ft_putnbr(n);
		}
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else if (n < 10)
	{
		d = n + '0';
		write(1, &d, 1);
	}
}
